//User function template for C++
class Solution{
public:	
	int maxSubstring(string s)
	{
	    int sweep = 0, ans = -1;
    for (auto c : s) {
        if (c == '0')sweep++;
        else sweep--;
        ans = max(ans, sweep);
        if (sweep < 0)sweep = 0;
    }
    return ans;
	}
};

